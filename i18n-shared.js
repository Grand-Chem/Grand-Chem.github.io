// Language system initialization
function initializeGCLLanguage() {
  let currentLanguage = localStorage.getItem('gcl-language') || 'en';

  function updateLanguageDisplay() {
    const currentLangSpan = document.getElementById('currentLang');
    if (currentLangSpan) {
      currentLangSpan.textContent = currentLanguage.toUpperCase();
    }

    // Update active state for language buttons
    document.querySelectorAll('[data-lang]').forEach(btn => {
      const lang = btn.getAttribute('data-lang');
      if (lang === currentLanguage) {
        btn.classList.add('bg-brand', 'text-white');
        btn.classList.remove('bg-neutral-100');
      } else {
        btn.classList.remove('bg-brand', 'text-white');
        btn.classList.add('bg-neutral-100');
      }
    });
  }

  function translatePage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
      const key = element.getAttribute('data-i18n');
      if (GCL_TRANSLATIONS[lang] && GCL_TRANSLATIONS[lang][key]) {
        element.textContent = GCL_TRANSLATIONS[lang][key];
      }
    });

    // Update HTML lang attribute
    document.documentElement.setAttribute('lang', lang);
  }

  function switchLanguage(newLang) {
    currentLanguage = newLang;
    localStorage.setItem('gcl-language', newLang);
    updateLanguageDisplay();
    translatePage(newLang);

    // Close language menu if open
    const langMenu = document.getElementById('langMenu');
    if (langMenu) {
      langMenu.classList.add('hidden');
    }
  }

  // Initialize
  updateLanguageDisplay();
  translatePage(currentLanguage);

  // Setup event listeners
  document.addEventListener('DOMContentLoaded', () => {
    // Language toggle button (desktop)
    const langToggle = document.getElementById('langToggle');
    const langMenu = document.getElementById('langMenu');

    if (langToggle && langMenu) {
      langToggle.addEventListener('click', () => {
        langMenu.classList.toggle('hidden');
      });

      // Close menu when clicking outside
      document.addEventListener('click', (e) => {
        if (!langToggle.contains(e.target) && !langMenu.contains(e.target)) {
          langMenu.classList.add('hidden');
        }
      });
    }

    // Language buttons (both desktop and mobile)
    document.querySelectorAll('[data-lang]').forEach(btn => {
      btn.addEventListener('click', () => {
        const lang = btn.getAttribute('data-lang');
        switchLanguage(lang);
      });
    });
  });
}

// Initialize when script loads
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initializeGCLLanguage);
} else {
  initializeGCLLanguage();
}
